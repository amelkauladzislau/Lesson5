public class Client {

    private static String name;
    private static String needFuel;
    private static String fuelType;

    public static void setName(String _name) {
        name = _name;
    }

    public static String getName() {
        return name;
    }

    public static void setNeedFuel(String _needFuel) {
        needFuel = _needFuel;
    }

    public static String getNeedFuel() {
        return needFuel;
    }

    public static void setfuelType(String _fuelType) {
        fuelType = _fuelType;
    }

    public static String getfuelType() {
        return fuelType;
    }

}
